const (
	userSetExample = `  {{.appName}} user set -t"User token"   set user token`
)

var setTokenCmd = &cobra.Command{
	Use:              "set",
	Aliases:          []string{"s"},
	Short:            "Set the current user token",
	Long:             `Set the current user token. The user token is generated by Wind River Studio Security Scanner. The user needs to open https://studio.windriver.com/scan/ and login, open the user profile page, then apply to generate the user token and copy it.`,
	Example:          formatExample(userSetExample),
	TraverseChildren: true,
	Run: func(cmd *cobra.Command, args []string) {
		executeUserSubCommand("set")
	},
}

